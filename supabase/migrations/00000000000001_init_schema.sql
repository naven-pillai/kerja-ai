-- ============================================================================
-- Kerja-AI — initial schema for a FRESH Supabase project.
-- AI/ML & data jobs for Malaysia & Singapore. Shared by kerja-ai (public)
-- and kerja-ai-admin. Jobs schema is intentionally identical to Kerja-Remote.
--
-- Unlike the Kerja-Remote project, RLS is ENABLED on every table from day one
-- with scoped policies (Kerja-Remote shipped GRANT ALL to anon with RLS off,
-- which exposed subscriber/candidate PII — this schema does not repeat that).
--
-- Apply to a new project:
--   supabase link --project-ref <NEW_REF>
--   supabase db push
-- ============================================================================

-- ---------------------------------------------------------------------------
-- Extensions & shared trigger functions
-- ---------------------------------------------------------------------------
create extension if not exists "pgcrypto";      -- gen_random_uuid()
create extension if not exists "uuid-ossp";      -- uuid_generate_v4()

create or replace function public.update_updated_at_column()
returns trigger language plpgsql as $$
begin
  new.updated_at = now();
  return new;
end;
$$;

-- Auto-set job expiry on insert (30 days, 60 if featured). Mirrors Kerja-Remote.
create or replace function public.set_job_expiry()
returns trigger language plpgsql as $$
begin
  new.expires_at := case
    when new.is_featured = true then now() + interval '60 days'
    else now() + interval '30 days'
  end;
  return new;
end;
$$;

