-- ---------------------------------------------------------------------------
-- job_ingestion  (NEW — scraper/AI extraction pipeline staging table)
-- The pipeline dedupes on source_url (unique) and fuzzy-dedupes in code on
-- (raw_title + raw_company + city). Rows are promoted into `jobs` on import.
-- ---------------------------------------------------------------------------
create table if not exists public.job_ingestion (
  id            uuid primary key default gen_random_uuid(),
  source_url    text not null unique,
  source_name   text,
  country       text check (country in ('MY','SG')),
  city          text,
  raw_title     text,
  raw_company   text,
  raw_location  text,
  extracted     jsonb,               -- Claude-extracted structured job payload
  status        text not null default 'pending'
                  check (status in ('pending','imported','rejected','duplicate','error')),
  job_id        uuid references public.jobs(id) on delete set null,
  error         text,
  created_at    timestamptz not null default now(),
  updated_at    timestamptz not null default now()
);
create index if not exists idx_job_ingestion_status on public.job_ingestion (status);
create trigger set_updated_at before update on public.job_ingestion
  for each row execute function public.update_updated_at_column();

