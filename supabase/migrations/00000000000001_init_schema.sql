create or replace view public.companies_with_job_count as
  select c.id, c.name, c.slug, c.logo_url, count(j.id) as job_count
  from public.companies c
  left join public.jobs j on j.company_id = c.id and j.status = 'published'
  group by c.id;

create or replace view public.sorted_companies as
  select id, name, lower(name) as name_lower, slug, website, tagline, logo_url, created_at
  from public.companies;

create or replace view public.jobs_with_payment as
  select j.id, j.title, j.slug, j.description, j.apply_url, j.company_id,
         j.job_type, j.job_category, j.job_location, j.min_salary, j.max_salary,
         j.currency, j.tags, j.status, j.is_featured, j.is_active, j.billing_plan,
         j.visibility_plan, j.seo_title, j.seo_description, j.goes_public_at,
         j.expires_at, j.valid_through, j.created_at, j.updated_at,
         p.email as paid_email, p.amount as paid_amount, p.currency as paid_currency,
         p.country as paid_country, p.payment_status as paid_payment_status,
         p.stripe_session_id, p.created_at as paid_created_at
  from public.jobs j
  left join public.job_payments p on p.job_id = j.id;

