-- ---------------------------------------------------------------------------
-- jobs  (schema identical to Kerja-Remote)
-- ---------------------------------------------------------------------------
create table if not exists public.jobs (
  id                   uuid primary key default gen_random_uuid(),
  slug                 text not null unique,
  title                text not null,
  description          text not null,
  job_type             text[] not null,
  job_category         text[] not null,
  job_location         text[] not null,
  min_salary           numeric,
  max_salary           numeric,
  currency             text not null default 'MYR',
  apply_url            text not null,
  tags                 text[],
  is_featured          boolean default false,
  is_active            boolean default true,
  company_id           uuid references public.companies(id) on delete set null,
  status               text default 'pending',
  remote_type          text not null default '100% Remote'
                         check (remote_type in ('100% Remote','Hybrid','Onsite')),
  billing_plan         text not null default 'free' check (billing_plan in ('free','paid')),
  visibility_plan      text not null default 'public'
                         check (visibility_plan in ('public','private_early_access')),
  seo_title            text,
  seo_description      text,
  goes_public_at       timestamptz default (now() + interval '12 hours'),
  expires_at           date,
  valid_through        date,
  -- monetization columns kept for schema parity (paid tier is a later phase)
  stripe_session_id    text,
  featured_addon_paid  boolean not null default false,
  paid_amount_total    integer,
  paid_currency        text,
  paid_at              timestamptz,
  created_at           timestamptz default timezone('utc', now()),
  updated_at           timestamp without time zone default now()
);
create index if not exists idx_jobs_status on public.jobs (status);
create index if not exists idx_jobs_company on public.jobs (company_id);
create trigger set_updated_at before update on public.jobs
  for each row execute function public.update_updated_at_column();
create trigger trigger_set_job_expiry before insert on public.jobs
  for each row execute function public.set_job_expiry();

