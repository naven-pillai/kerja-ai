alter table public.newsletter_subscribers enable row level security;
alter table public.job_ingestion         enable row level security;

-- Public (anon) read of published/public content -----------------------------
create policy "anon read published jobs" on public.jobs
  for select to anon using (status = 'published' and goes_public_at <= now());
create policy "anon read published blogs" on public.blogs
  for select to anon using (status = 'published');
create policy "anon read companies"  on public.companies  for select to anon using (true);
create policy "anon read authors"     on public.authors    for select to anon using (true);
create policy "anon read categories"  on public.categories for select to anon using (true);

-- Public job submission goes through the /api/submit-job server route (service
-- role), so anon does NOT insert jobs/companies directly.
-- Analytics: anyone can log a view/click, nobody anon can read the stream.
create policy "anon insert job events" on public.job_events
  for insert to anon with check (true);

-- Authenticated (admin) full access ------------------------------------------
create policy "auth full jobs"        on public.jobs                  to authenticated using (true) with check (true);
create policy "auth full companies"   on public.companies             to authenticated using (true) with check (true);
create policy "auth full blogs"       on public.blogs                 to authenticated using (true) with check (true);
create policy "auth full authors"     on public.authors               to authenticated using (true) with check (true);
create policy "auth full categories"  on public.categories            to authenticated using (true) with check (true);
create policy "auth read events"      on public.job_events            for select to authenticated using (true);
create policy "auth full payments"    on public.job_payments          to authenticated using (true) with check (true);
create policy "auth full subscribers" on public.newsletter_subscribers to authenticated using (true) with check (true);
create policy "auth full ingestion"   on public.job_ingestion         to authenticated using (true) with check (true);

-- ---------------------------------------------------------------------------
-- Grants. RLS still governs row visibility; these just expose the tables to
-- the PostgREST roles. Note: NO grants to anon on subscriber/payment/ingestion.
-- ---------------------------------------------------------------------------
grant usage on schema public to anon, authenticated, service_role;

grant select on public.jobs, public.blogs, public.companies, public.authors,
  public.categories, public.companies_with_job_count, public.sorted_companies,
  public.jobs_with_payment to anon;
grant insert on public.job_events to anon;

grant all on public.jobs, public.companies, public.blogs, public.authors,
  public.categories, public.job_events, public.job_payments,
  public.newsletter_subscribers, public.job_ingestion to authenticated, service_role;
grant select on public.companies_with_job_count, public.sorted_companies,
  public.jobs_with_payment to authenticated, service_role;

grant execute on function public.get_jobs_stats(uuid[]), public.job_events_daily(timestamptz, timestamptz),
  public.top_jobs_by_event(text, integer),
  public.top_jobs_by_event_windowed(text, timestamptz, timestamptz, integer) to anon, authenticated, service_role;

